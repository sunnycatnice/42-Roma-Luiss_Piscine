int	ft_str_is_alpha(char *str)
{
	int i;

	i = 0;
	while ((str[i] >= 65 && str[i] <= 90) || (str[i] >= 97 && str[i] <= 112))
		i++;
	if (str[i] == '\0')
		return (1);
	return (0);
}
